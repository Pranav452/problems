name = "Alice"
age = 28
city = "Delhi"

# Using % formatting
print("My name is %s, I am %d years old, and I live in %s." % (name, age, city))

# Using .format() method
print("My name is {}, I am {} years old, and I live in {}.".format(name, age, city))

# Using f-strings
print(f"My name is {name}, I am {age} years old, and I live in {city}.")